package vo;

public class PwHistory {
	private int pwNo;
	private String id;
	private String pw;
	private String createdate;
	
	public int getPwNo() {
		return pwNo;
	}
	public void setPwNo(int pwNo) {
		this.pwNo = pwNo;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getPw() {
		return pw;
	}
	public void setPw(String pw) {
		this.pw = pw;
	}
	public String getCreatedate() {
		return createdate;
	}
	public void setCreatedate(String createdate) {
		this.createdate = createdate;
	}
}
